<script type="text/javascript">
    // VALIDATOR
    jQuery(document).ready(function($) {
        $("#form_contact").validate({
            rules: {
                name: "required",
                content: "required",
            },
            messages: {
                name: "Please enter your name!",
                content: "Please enter your content message!",
            },
            submitHandler : function(e){
                var data = jQuery("#form_contact").serialize();
                var over = $('<div id="overlay">' +
                        '<img id="loading" src="<?php echo get_template_directory_uri() ?>/images/supersized/progress.gif">' +
                    '</div>');
                jQuery.ajax({
                    type: "post",
                    dataType: "json",
                    data: data,
                    url: "<?php echo get_bloginfo('home')."/wp-admin/admin-ajax.php"; ?>",
                    beforeSend: function() {
                        jQuery("#form_contact").append(over);
                    },
                    success: function(data) {
                        over.remove();
                        var status = data.status;
                        if(status == '1'){
                            jQuery("#form_contact .show-flash-msg span").show().html('Something maybe wrong, please resend again!').fadeOut(5000)
                        }else if(status == '2'){
                            jQuery("#form_contact .show-flash-msg span").show().html('Something maybe wrong, please resend again!').fadeOut(5000)
                        }else if(status == '0'){
                            jQuery("#form_contact .show-flash-msg span").show()
                                .html('Thanks for contact with us!')
                                .css('color', '#BF2626')
                                .fadeOut(5000, function(){
                                    window.location.href=window.location.href;
                                });
                            jQuery("#form_contact").find('input,textarea').val('');

                        }else{
                            jQuery("#form_contact .show-flash-msg span").show().html('Something maybe wrong, please resend again!').fadeOut(5000)
                        }
                    },
                    error: function() {
                        over.remove();
                        jQuery("#form_contact .show-flash-msg span").show().html('Something maybe wrong, please resend again!').fadeOut(5000)
                    },
                    complete : function(){
                        over.remove();
                    }
                });
                  return false; 
            }
        });
    });
</script>

